package com.squid.kraken.v4.model;

import java.io.Serializable;

/**
 * Defines an access right of a User or a UserGroup.
 */
@SuppressWarnings("serial")
public class AccessRight implements Serializable {

    static public enum Role {
        NONE, READ, WRITE, OWNER
    };

    private Role role;

    private String userId;

    private String groupId;

    public AccessRight() {
        super();
    }

    /**
     * Constructor.<br>
     * UserId or groupId must be set, but not both.
     * 
     * @param role
     *            the role to assign
     * @param userId
     *            the userId assigned (can be null)
     * @param groupId
     *            the group (id) assigned (can be null)
     */
    public AccessRight(Role role, String userId, String groupId) {
        super();
        this.role = role;
        this.userId = userId;
        this.groupId = groupId;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((groupId == null) ? 0 : groupId.hashCode());
        result = prime * result + ((role == null) ? 0 : role.hashCode());
        result = prime * result + ((userId == null) ? 0 : userId.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        AccessRight other = (AccessRight) obj;
        if (groupId == null) {
            if (other.groupId != null)
                return false;
        } else if (!groupId.equals(other.groupId))
            return false;
        if (role == null) {
            if (other.role != null)
                return false;
        } else if (!role.equals(other.role))
            return false;
        if (userId == null) {
            if (other.userId != null)
                return false;
        } else if (!userId.equals(other.userId))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return "AccessRight [role=" + role + ", userId=" + userId + ", groupId=" + groupId + "]";
    }

}
